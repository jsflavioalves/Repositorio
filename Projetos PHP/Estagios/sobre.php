				<h2>UFC - UNIVERSIDADE FEDERAL DO CEARÁ</h2>
		
				<p style="text-align: justify;">Atualmente, a UFC está presente na Zona Norte do Estado, Sertão Central, Inhamuns, Vale do Jaguaribe e Região Metropolitana de Fortaleza. Com o objetivo de disciplinar o Programa de Estágio Curricular Supervisionado em todas as unidades acadêmicas da UFC, foi criada a Agência de Estágios da UFC, vinculada à Pró-Reitoria de Extensão (PREx), a qual é responsável pela articulação, agenciamento e formalização dos estágios obrigatórios e não obrigatórios junto a empresas e demais organizações conveniadas.</p>
				
				<br />
				
				
				<h2>﻿Serviços</h2>
				
				
									<p> - Divulgação de Vagas de estágio.</p>
									<p> - Divulgação de trainee.</p>
									<p> - Intermediação de termos de convênio entre a UFC e as concedentes de estágios.</p>
									<p> - Orientação sobre os cursos que atendem o perfil da vaga solicitada.</p>
									
				
				<br />
				
				
				<h2>Visão</h2>
				
				<p><i class="fa fa-check"></i> Ser referência no Estado do Ceará como um agente facilitador entre os estudantes de graduação da UFC e as instituições concedentes de estágios.</p>

				<br />
				
				
				<h2>Missão</h2>
		
				<p><i class="fa fa-check"></i>  Contribuir para a sólida formação dos estudantes da UFC promovendo a interação entre a Universidade e as Instituições e oportunizando o contato inicial com o mundo do trabalho por meio do estágio.</p>
				
				<br />
				
				
				<h2>Valores</h2>
		
				<p><i class="fa fa-check"></i>  Eficiência, Responsabilidade, Comprometimento, Impessoalidade, Transparência, Proatividade e Parceria.</p>

				<br />

				<h2>Objetivos</h2>
		
									<p><i class="fa fa-check"></i>  Promover ao estudante oportunidade de colocar em prática os conhecimentos teóricos adquiridos em seu curso universitário;</p>
									<p><i class="fa fa-check"></i>  Fornecer ao estagiário uma visão macro da Empresa, sua estrutura organizacional/funcional;<p>
									<p><i class="fa fa-check"></i>  Proporcionar para os alunos a oportunidade de estágios para aplicar os conhecimentos científicos e tecnológicos;<p>
									<p><i class="fa fa-check"></i>  Desenvolver a capacidade de visão crítica, técnica, política e humanista da sociedade. <p>
								

