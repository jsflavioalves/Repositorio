<p align="justify">	
Em sua primeira edição, o Encontro de Estágios da UFC integrará os Encontros Universitários 2017, trazendo a possibilidade dos estudantes apresentarem relatos de suas experiências de estágio e do público conhecer as atividades desenvolvidas pelas empresas e instituições conveniadas. Além das apresentações orais e das exposições de pôsteres, o evento contará com palestras e com a participação dos agentes de integração e empresas parceiras.</p>

<p align="justify">
O diferencial do Encontro de Estágios é que, para inscrição de trabalhos, não foi exigida a condição de bolsista em algum projeto, bastando que o estudante tivesse desenvolvido, ao longo deste ano, atividade de estágio devidamente formalizada e registrada junto à Agência de Estágios da UFC. Desse modo, o evento despertou o interesse de diversos alunos, totalizando 56 apresentações orais e 35 pôsteres.</p>

<p align="justify">
Assim, a manhã do dia 10.11 (sexta-feira) será dedica à realização de duas palestras abertas ao público. A primeira, iniciando às 08 (oito) horas, será ministrada pelo Professor Rogério Teixeira Mâsih, Coordenador da Agência de Estágios da UFC, com a temática “Tudo que você precisa saber sobre estágio”. Logo em seguida, ocorrerá a palestra “IBM Brasil – Programas de Estágio e Trainee”, apresentada pelo Arquiteto de Soluções Analíticas e Cognitivas da empresa, Marcus Eugênio Façanha Braga.</p>

<p align="justify">
Para os que ainda buscam uma oportunidade de estágio, também na sexta-feira pela manhã, estarão presentes os agentes de integração que, geralmente atuando como auxiliares no processo de contratação, realizam o recrutamento e a seleção de estudantes para empresas interessadas. Sugere-se, portanto, que os alunos compareçam com currículos atualizados.</p>

<p align="justify">
Toda a programação do I Encontro de Estágios ocorrerá no Centro de Convivência do Campus do Pici, onde também estará disponível uma sala de apoio da organização para orientar os participantes e o público.</p>

<p align="justify">
Esperamos que, com o sucesso da 1° Edição do evento, o Encontro de Estágios se consolide no cronograma dos Encontros Universitários, crescendo a cada ano em número de trabalhos, de empresas e de agentes de integração participantes e ampliando as oportunidades de estágio aos estudantes das UFC. </p>

